    <h2>What is oil trading?</h2>

<p>Oil trading is the buying and selling of different types of oil and oil-linked assets with the aim of making a profit.
As oil is a finite resource, its price can see massive fluctuations due to supply and demand changes. This volatility
makes it extremely popular among traders.</p>

<p>You can use CFDs to trade on oil’s spot price, or the prices of oil futures or options contracts, without having to own
